    public function ban(Request $request)
    {

        $user = User::where('id', $request->uuid)->firstOrFail();

        if($user->status){
            $user->update([
                "status" => 0
            ]);
        } else{
            $user->update([
                "status" => 1
            ]);
        }
        
        return redirect()->route('index');
    }


